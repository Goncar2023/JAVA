    //Para CONCATENAR cadenas y variables se utiliza el operador + 

    //Declaración y asignacion de una variable String
    String nombreUsuario = "Hugo2023";
    
    /*
        TIPOS DE VARIABLES
    */
    
    //NUMÉRICAS ENTERAS -> Almacenan números enteros sin parte decimal
